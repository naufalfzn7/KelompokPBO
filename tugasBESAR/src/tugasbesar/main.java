    private void SUBMITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SUBMITActionPerformed
        String metode = (String) jComboMetode.getSelectedItem();
        String jenis = (String) jComboJenis.getSelectedItem();
        String barang = (String) jComboBarang.getSelectedItem();
        double nominal = Double.parseDouble(TfPembayaran.getText());
        
        if (barang.equals("TELEVISI")){
            if (nominal < televisi) {
                JOptionPane.showMessageDialog(null, "MAAF UANGMU TIDAK CUKUP");
            }
            
            else if (nominal >= televisi) {
                double kembalian = nominal - televisi;
                JOptionPane.showMessageDialog(null, "SELAMAT TELAH BERHASIL MEMBELI "+barang+ "\nMENGGUNAKAN METODE : " + metode + "\nJENIS TRANSAKSI : " + jenis + "\nTOTAL KEMBALIAN : " + kembalian);
            }
        }
        
        else if (barang.equals("MICROWAVE")) {
            if (nominal < microwave) {
                JOptionPane.showMessageDialog(null, "MAAF UANGMU TIDAK CUKUP");
            }
            
            else if (nominal >= microwave) {
                double kembalian = nominal - microwave;
                JOptionPane.showMessageDialog(null, "SELAMAT  TELAH BERHASIL MEMBELI "+ barang +"\nMENGGUNAKAN METODE : " + metode + "\nJENIS TRANSAKSI : " + jenis + "\nTOTAL KEMBALIAN : " + kembalian);
            }
        }
        
        else if (barang.equals("AC")) {
            if (nominal < ac) {
                JOptionPane.showMessageDialog(null, "MAAF UANGMU TIDAK CUKUP");
            }
            
            else if (nominal >= ac) {
                double kembalian = nominal - ac;
                JOptionPane.showMessageDialog(null, "SELAMAT TELAH BERHASIL MEMBELI "+ barang +"\nMENGGUNAKAN METODE : " + metode + "\nJENIS TRANSAKSI : " + jenis + "\nTOTAL KEMBALIAN : " + kembalian);
            }
        
        
        }
        
        else if (barang.equals("KULKAS")) {
            if (nominal < kulkas) {
                JOptionPane.showMessageDialog(null, "MAAF UANGMU TIDAK CUKUP");
            }
            
            else if (nominal >= kulkas) {
                double kembalian = nominal - kulkas;
                JOptionPane.showMessageDialog(null, "SELAMAT TELAH BERHASIL MEMBELI "+ barang +"\nMENGGUNAKAN METODE : " + metode + "\nJENIS TRANSAKSI : " + jenis + "\nTOTAL KEMBALIAN : " + kembalian);
            }
        
        
        }
        
        
        
        
        
        
    }//GEN-LAST:event_SUBMITActionPerformed

